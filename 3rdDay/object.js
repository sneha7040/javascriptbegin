let car = {
    brand_name:"renault",
    model_name:"kwid",
    model_no:"4646"

}
console.log(car);
  

// how to change key value pairs in js 
car.model_no="7998";
console.log(car);


// how to add key value pairs to an object in js using dot notation
car.owner_name = "sneha";
console.log(car);


// we can also add data through bracket notation 
car["price"] = ["4,30,000"];
console.log(car);















